export default class User {
  constructor(
    name,
    gender,
    city,
    phone,
    date,
    username,
    password,
    role,
    token,
    id
  ) {
    this.name = name;
    this.gender = gender;
    this.city = city;
    this.phone = phone;
    this.date = date;
    this.username = username;
    this.password = password;
    this.role = role;
    this.token = token;
    this.id = id;
  }
}
